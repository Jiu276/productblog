export function getArticleById(id: string): BlogPost | undefined {
  return articles.find(article => article.id === id)
}

export function getArticleBySlug(slug: string): BlogPost | undefined {
  return articles.find(article => article.slug === slug)
}

export function getArticlesByCategory(category: string): BlogPost[] {
  return articles.filter(article => article.category === category)
}

export function getArticlesByTag(tag: string): BlogPost[] {
  return articles.filter(article => article.tags.includes(tag))
}

export function getFeaturedArticles(limit: number = 3): BlogPost[] {
  return articles
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, limit)
}

export function getRecentArticles(limit: number = 6): BlogPost[] {
  return articles
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, limit)
}